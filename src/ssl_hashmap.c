static unsigned long	hash(unsigned char *str)
{
	unsigned long hash;

	hash = 5381;
	while (*str)
	{
		hash = ((hash << 5U) + hash) ^ *str;
		str++;
	}
	return (hash);
}

char					*ssl_strategy(char **cmd)
{
	t_fnx	*blk;

	if ((blk = &g_ssl_hmap[hash((unsigned char *)*cmd) % 128])->name)
		if (!ft_strcmp(blk->name, *cmd))
			return (blk->f(cmd + 1));
	ssl_invalidcmd(*cmd);
	return (0);
}
